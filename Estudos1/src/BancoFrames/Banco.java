    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Cliente c = new Cliente();
        c.setCpf(cpf.getText());
        c.setEmail(email.getText());
        c.setIdade(Integer.parseInt(idade.getText()));
        c.setNome(nome.getText());
        c.setRG(rg.getText());
        c.setTelefone(telefone.getText());
        
        clientes.add(c);
        JOptionPane.showMessageDialog(null, "Cliente Cadastrado");
        carregarTabelaDeClientes();
        nome.setText("");
        email.setText("");
        cpf.setText("");
        idade.setText("");
        rg.setText("");
        telefone.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        ContaBancaria c = new ContaBancaria();
        c.setSaldo(Float.parseFloat(saldo.getText()));
        c.setCliente(clientes.get(TabelaDeClientes.getSelectedRow()));
        contas.add(c);
        JOptionPane.showMessageDialog(null, "Conta Adicionada com Sucesso!");
        carregarTabelaDeContas();
    }//GEN-LAST:event_jButton2ActionPerformed

