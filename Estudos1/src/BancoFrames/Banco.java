    private void carregarTabelaDeClientes(){
        DefaultTableModel model = (DefaultTableModel) TabelaDeClientes.getModel();
        model.setNumRows(0);
        for(Cliente c: clientes) model.addRow(new Object[]{c.getNome(), c.getEmail(), c.getCpf()});
    }

    private void carregarTabelaDeContas(){
        DefaultTableModel model = (DefaultTableModel) TabelaDeConta.getModel();
        model.setNumRows(0);
        for(ContaBancaria c: contas) model.addRow(new Object[]{c.getCliente().getNome(), c.getSaldo()});
    }
    
    
